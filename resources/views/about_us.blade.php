<p><b>(EO No. 11A, Series of 2017)</b> Amending Section 4. Executive Order NR 11 (S.2017) In Relations To Its Coverage And Compliance.</p>
<p><b>(DO No. 39, Series of 2020)</b> Revised Construction Safety Guidelines For The Implementation Of Infrastructure Projects During Covid-19 Public Health Crisis, Repealing Department Order No. Series Of 2020.</p>

<h2><u>Service Pledge</u></h2>

<p>Implement the provisions of the National Building Code of the Philippines, and the Local Building Code of Quezon City, its referral codes and other related laws and ordinances.</p>

<p>Evaluate, process and issue building permits, including ancillary and auxiliary permit applications, for all types of buildings and/or structures as required under the National Building Code of the Philippines, and the Local Building Code of Quezon City when the minimum requirements are satisfied.</p>

<p>Conduct ocular inspections on on-going and completed structures as a requirement in the processing and issuance of occupancy and other related final permits as well as conduct annual inspections of building and other structures to ensure compliance to safety standards to prevent unnecessary loss of life and property.</p>

<p>Evaluate, process and issue excavation permits for public utilities.</p>

<p>Plan, design and impose building regulatory measures and parameters needed to enhance the city development plans and programs.</p>

<p>Entertain complaints, conduct administrative summary proceedings, and issue stoppage Orders and Resolutions in the exercise of quasi-judicial functions.</p>


