    <h2><u>Description</u></h2>
    <p>Under the legal mandate, the Department of the Building Official implements the National Building Code of the Philippines, its referral codes and other laws and ordinances related thereto; Processes and issues building permits, including ancillary and auxiliary permit applications for all types of structures as required under the code; Conducts inspection of on-going and completed structures as a requirement in the processing and issuance of occupancy and other final permits related thereto; conducts annual inspection of building and other structures to ensure compliance to safety standards to prevent unnecessary loss of life and property; Processes and issues excavation permits for public utilities; Plans, design and imposes building regulatory measures and parameters needed to enhance the city development plans and programs.</p>

<h2><u>Vision</u></h2>
    <p>To effectively implement building safety standards and ensure that buildings and structures in the Quezon City are not only livable but are properly built and maintained.</p>

<h2><u>Mission</u></h2>
    <p>To render fast and efficient service in the evaluation of building permits without sacrificing safety and ensure compliance of building owners to safety standards through intensified safety inspections.</p>    

<h2><u>Legal Bases</u></h2>
    <p>The provisions of the National Building Code of the Philippines (NBCP) (P.D.1096) and its Implementing Rules and Regulations (IRR), and the provisions of the Local Building Code of Quezon City (City Ordinance No. SP-2518, S-2016) and its Implementing Rules and Regulations (IRR) provides the Quezon City Building Official the authority to carry out their respective provisions within the territorial jurisdiction of Quezon City.</p>

<h2 style="color: #1484d9;" >  Executive Orders:</h2>
